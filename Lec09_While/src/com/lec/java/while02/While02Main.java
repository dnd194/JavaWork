package com.lec.java.while02;

public class While02Main {

	public static void main(String[] args) {
		System.out.println("while 연습");
		// 99단 2단

		int i = 0;
		while (i <= 9) {
			System.out.println("2 x " + i + " = " + (2 * i));
			i++;
		}

	} // end main()

} // end class While02Main
